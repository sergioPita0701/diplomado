                            <p>El Diplomado en Docencia para Educacion Superior - Modalidad Virtual - 
                            Misión  
Desarrolla en los profesionales procesos de enseñanza y procesos de aprendizaje, así como formaciones teóricas, pedagógicas, psicológicas y epistemológico, la metodología y los problemas de área del conocimiento específico, ya que con ello comienza su proceso de formación como investigador y Docente en Educación Superior con conocimientos socialmente válidos, con un personal competente y comprometido con los valores que promueve el postgrado de la Facultad de Pedagogía. 
Visión  
Formar profesionales de calidad en el nivel posgradual de Docencia para Educación Superior, bajo fundamentos de equidad y oportunidad que sustenta la educación permanente, recreando la actividad académica, fortaleciendo la investigación, la transferencia tecnológica y el saber en procesos activos de interacción social , contribuyendo así al desarrollo económico, productivo y social tanto a nivel local, nacional como internacional. 
Objetivos 
Desarrollar competentes profesionales para el ejercicio de la docencia en el Nivel de Educación Superior mediante una práctica pedagógica científica, contextualizada e innovadora que propicie un aprendizaje significativo y transformador. 
